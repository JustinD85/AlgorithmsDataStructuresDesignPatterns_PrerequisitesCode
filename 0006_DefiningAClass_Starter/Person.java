
public class Person
{
    private String name;
    private int age;
    
    public int getAge()
    {
        return age;
    }
    
    public void setAge(int value)
    {
        age = value;
    }
    
    public String getName()
    {
        return name;
    }
    
    public void setName(String value)
    {
        name = value;
    }
}
